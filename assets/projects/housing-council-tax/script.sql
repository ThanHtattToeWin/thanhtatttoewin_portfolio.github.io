BEGIN TRANSACTION;
CREATE TABLE IF NOT EXISTS Bands (
	Band	char(5) NOT NULL PRIMARY KEY,
	Fraction	char(5) NOT NULL
);
CREATE TABLE IF NOT EXISTS CouncilTax (
    Location_ID char(10) NOT NULL,
    Band char(5) NOT NULL,
    CouncilTax real NOT NULL,
    PRIMARY KEY (Location_ID, Band),
    FOREIGN KEY (Location_ID) REFERENCES Location(Location_ID),
    FOREIGN KEY (Band) REFERENCES Bands(Band)
);
CREATE TABLE IF NOT EXISTS LocalAuthority (
	Local_Authority_Code	char(20) NOT NULL PRIMARY KEY,
	Local_Authority_Name	char(50) NOT NULL
);
CREATE TABLE IF NOT EXISTS Location(
	Location_ID	char(10) NOT NULL PRIMARY KEY,
	Location_Name char(50) NOT NULL,
	Local_Authority_Code	char(20) NOT NULL,	
	FOREIGN KEY (Local_Authority_Code) REFERENCES LocalAuthority(Local_Authority_Code)
);
CREATE TABLE IF NOT EXISTS MedianHousePrice (
	Median_Price_ID	int NOT NULL PRIMARY KEY,
	Median_Price real NOT NULL,
	Year_Ending	date NOT NULL,
	Ward_Code	char(20) NOT NULL,
	FOREIGN KEY (Ward_Code) REFERENCES Ward(Ward_Code)
);
CREATE TABLE IF NOT EXISTS Ward (
	Ward_Code	char(20) NOT NULL PRIMARY KEY,
	Ward_Name	char(100) NOT NULL,
	Local_Authority_Code	char(20) NOT NULL,
	FOREIGN KEY (Local_Authority_Code) REFERENCES LocalAuthority(Local_Authority_Code)
);
